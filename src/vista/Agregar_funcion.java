    private void ButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAgregarActionPerformed
        
            if (verificaVacios1()) {
                Date fecha = DCfecha1.getDate();
                long d = fecha.getTime();
                java.sql.Date fecha2 = new java.sql.Date(d);
                String horainicio = TXThi1.getText();
                String horafinal = TXThf1.getText();
                int activacion = Comboacti_fun.getSelectedIndex();
                int sala = salacombox.getSelectedIndex();
                int pelicula = pelibox.getSelectedIndex();
                FuncionE funciones = new FuncionE(fecha2, horainicio, horafinal, activacion, sala, pelicula);
                if (this.getcFuncion().agregarFuncion(funciones)) {

                    JOptionPane.showMessageDialog(null, "Funcion agregada");

                } else {
                    JOptionPane.showMessageDialog(null, "Funcion no agregada");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor rellene la información");
            }
        
    }//GEN-LAST:event_ButtonAgregarActionPerformed

