    private void Cargarsala() {
        ControladorSala csa = new ControladorSala();
        List<Sala> lista = csa.listarSala();
        for (int i = 0; i < lista.size(); i = i + 1) {
            Sala sal = new Sala(lista.get(i).getId_sala());
            salacombox.addItem(String.valueOf(sal.getId_sala()));
            CBsala.addItem(String.valueOf(sal.getId_sala()));
        }
    }

    private void Cargarpelicula() {
        ControladorPelicula ple = new ControladorPelicula();
        List<Pelicula> lista = ple.listarPeliculas();
        for (int i = 0; i < lista.size(); i = i + 1) {
            Pelicula pel = new Pelicula(lista.get(i).getId_pelicula(), lista.get(i).getNombre());
            pelibox.addItem(String.valueOf(pel.getId_pelicula()));
            pelibox1.addItem(String.valueOf(pel.getId_pelicula()));
        }
    }

    private void llenarTablaFun() {
        //jTable1
        String[] columnas = {"id_funcion", "Fecha", "Horainicio", "Horafinal", "Acti_funcion", "id_sala", "Id_pelicula"};
        DefaultTableModel dModel = new DefaultTableModel(columnas, 0);
        List<FuncionE> funciones = this.getcFuncion().listarFunciones();
        for (FuncionE funcione : funciones) {
            dModel.addRow(funcione.datosTabla());
        }
        jTable1.setModel(dModel);
    }

