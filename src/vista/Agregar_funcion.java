    private void ButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBuscarActionPerformed
        FuncionE fun = this.getcFuncion().buscarFuncion(Integer.parseInt(TXTid.getText()));
        if (fun == null) {
            JOptionPane.showMessageDialog(null, "Funcion no existe");
        } else {
            System.out.println("Funcion encontrada");
            DCfecha2.setDate(fun.getFecha());
            TXThi2.setText(fun.getHorainicio());
            TXThf2.setText(fun.getHorafinal());
            CBsala.setSelectedIndex(fun.getSala());
            pelibox1.setSelectedIndex(fun.getId_pelicula());
            activo.setSelectedIndex(fun.getActi_fun());
        }
    }//GEN-LAST:event_ButtonBuscarActionPerformed

    private void ButtonEliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEliActionPerformed
        if (!TXTid.getText().equals("")) {
            if (this.getcFuncion().eliminarFuncion(Integer.parseInt(TXTid.getText()))) {
                JOptionPane.showMessageDialog(null, "Funcion eliminada");
            } else {
                JOptionPane.showMessageDialog(null, "No se elimino");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese una id para eliminar la función");
        }
    }//GEN-LAST:event_ButtonEliActionPerformed

