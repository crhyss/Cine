    private void ButtonIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonIniciarActionPerformed
        if (this.modoUsuario) {
            if (TXTpass.getText().equals("") && TXTnom_u.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Por favor ingrese algún dato para procesar");
            } else if (TXTnom_u.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Por favor ingrese un nombre de usuario");
            } else if (TXTpass.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Por favor ingrese una contraseña");
            } else if (!TXTpass.getText().equals("") && !TXTnom_u.getText().equals("")) {
                if (cu.verificaExistencia(TXTnom_u.getText(), TXTpass.getText())) {
                    dispose();
                    Menu_admin menuA = new Menu_admin();
                    Menu_usuario.run.setText(TXTnom_u.getText());
                    menuA.show();
                } else {
                    limpiar();
                    JOptionPane.showMessageDialog(null, "Nombre de usuario o contraseña incorrecta");
                }
            }

        } else if (!this.modoUsuario) {
            if (TXTnom_u.getText().equals("")) {
                limpiar();
                JOptionPane.showMessageDialog(null, "Por favor ingrese su RUN");
            } else if (!TXTnom_u.getText().equals("")) {
                if (cc.verificaExistencia(cp.obtenerId_persona(TXTnom_u.getText()))) {
                    dispose();
                    Menu_usuario menuC = new Menu_usuario();
                    Menu_usuario.run.setText(TXTnom_u.getText());
                    menuC.show();
                } else {
                    limpiar();
                    JOptionPane.showMessageDialog(null, "RUN ingresado incorrecto");
                }
            }
        }
    }//GEN-LAST:event_ButtonIniciarActionPerformed

