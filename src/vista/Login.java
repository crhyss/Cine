    private void ButtonModoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonModoActionPerformed
        if (this.modoUsuario) {
            this.modoUsuario = false;
            TXTpass.setVisible(false);
            labelPass.setVisible(false);
            labelRegistra.setVisible(true);
            ButtonRegistrar.setVisible(true);
            labelNom.setText("RUN (Formato: 12345678-9)");
            ButtonModo.setText("Modo Cliente");
        } else if (!modoUsuario) {
            this.modoUsuario = true;
            TXTpass.setVisible(true);
            labelPass.setVisible(true);
            labelRegistra.setVisible(false);
            ButtonRegistrar.setVisible(false);
            labelNom.setText("Usuario");
            ButtonModo.setText("Modo Usuario");
        }
    }//GEN-LAST:event_ButtonModoActionPerformed

